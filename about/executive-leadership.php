<hr /><p >Over the last 6 years, WUHS (Belize) leadership and staff have engaged in a comprehensive strategic planning effort that will guide the medical school for the next ten years.</p>

<p >At the same time, our leadership and faculty from our partners in Belize as well as leaders from the clinical practice community and public policymakers have convened to evaluate our programs in primary care and medical education.</p>

<p >The goals for all of these efforts – collectively called Belize for the 21st century, for we are committed to becoming the preferred Academic Medical University for the 21st century in the country of Belize — are to build on our strengths, agree on areas that need strengthening, and commit our collective resources to sustainable excellence in each area of our mission.</p>

<p >Through strategic investment of institutional resources – financial and intellectual, from the University, our partners and the Foundation, our practice plan – and leveraged by transformational philanthropic investment, we can – and will – become the preferred academic medical center of the 21st century in Belize. </p>

<p >Other key elements of the plan include research infrastructure and technology platforms. To guide investment, Steering Committees in those areas worked to quantify and prioritize their ambitious plans for excellence throughout the spring and summer.As a result, we have a broad scope of investments, all aimed at empowering our faculty to achieve new heights of excellence.</p>

<p >Primary care and medical education committees have also prioritized their needs and opportunities, and we are making strategic investments in those areas as well.
Our success is predicated on our people – our students, faculty and staff, and our leadership at the department, division and center level. We are working every day to recruit and retain the best in each of those areas, and remain committed to becoming the preferred Academic Medical Center of the 21st century in the Caribbean region, Central America and Belize.</p>

