       <p>Rather than just one hospital, you’ll train in several.</br>
        Our distributed hospital system connects you with patients from all walks of life, and with a wide range of medical conditions.</br>
        You’ll adapt to a variety of health care settings while increasing the depth and breath of your knowledge from our expert faculty practicing throughout the region.</p>
		<h3>Get a Quality Education–Affordably</h3>
