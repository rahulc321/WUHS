      <p class="small">Several Scholarships are available from the University’s Institutional Aid Programs for the Pre-Clinical Science portion of the curriculum. Those students who feel they can demonstrate financial need, and/or academic excellence and meet the other criteria for one or more programs are encouraged to apply. Although an application may be reviewed for several programs, a recipient generally will receive only one type of scholarship.
Tuition scholarships are granted to members of each entering class. Each Scholarship Recipient is awarded up to 50% rebate of the tuition. Awards for the full cost of attendance are not offered. In order for the student to plan in advance for the financial obligations associated with attending WUHS (Belize), most applications and awards are made prior to the student’s matriculation in the school or for the entire length of the program.
WUHS (Belize’s) Committees and Departments, in accordance with guidelines set by the Office of Financial Aid, makes the selection of scholarship recipients. Award determinations are based on academic excellence, the degree of the family’s financial need and the availability of funds from the School as well as the scholarship’s specified criteria. All awards must be used to defray the cost of tuition and are credited directly to the student’s account each term by the Office of Student Finances.
Applications for the programs can be downloaded from the University website, submitted online or requested to be sent by mail along with the Financial Aid Package. Applications are reviewed on a rolling basis up until the first day of classes. Applicants will receive either an award letter or a letter of declination from the Office of Financial Aid. Award letter will contain the scholarship amount and terms.</p>

<hr /><h2 class="large">Admission Excellence Awards</h2>

<p class="small">The Award targets prospective students, who have demonstrated excellent academic achievement prior to the acceptance to WUHS (Belize) or attained strong scores on the Medical College Admissions Test (MCAT).
All admitted applicants who meet the Award requirements are automatically considered. A separate application is not required. Applicants will be awarded approximately one months prior to the semester start date as long as the funds are available for a given semester, therefore applying early may significantly increase the chances of approval. Admission Excellence Award may not be deferred to another semester.</p>
<hr /><h2 class="large">Academic Excellence Scholarships</h2>

<p class="small">Each semester several tuition scholarships will be awarded to students, who have achieved academic excellence during their studies. Recipients of these scholarships must also meet the high moral and ethical standards set by the Student Honor Code. The Dean of Premedical Program and the Dean of Basic Science make the final decision on applications for these scholarships.</p>
<hr /><h2 class="large">Clinical Experience Scholarships</h2>

<p class="small">This scholarship is dedicated to the Dean of Clinical Science, a leader in the field of medical education in the United States, who had a major impact on the development of WUHS (Belize’s) Clinical Rotations. These partial tuition scholarships are awarded in some situations to students, who passed USMLE Step 1 with high scores or demonstrated outstanding achievements during the Clinical Science Program.</p>
<hr /><h2 class="large">Medical Community Scholarships</h2>

<p class="small">This program is committed to promoting a student body made up of diverse nationalities and cultural backgrounds, which in the future will contribute to a worldwide medical community. Partial scholarships are awarded to non-US and non-Canadian students, who exhibit academic excellence, prove financial need, and who will return to their home countries as physicians dedicated to the enhancement of the countries’ medical care systems.</p>
<hr /><h2 class="large">SGA Scholarship</h2>

<p class="small">One scholarship per semester will be granted to a current or former member of the Student Government Association, who has participated in extracurricular activities, community services or WUHS (Belize) developments. Applicants must have been associated with SGA for at least one full semester.</p>
