	<p class="large">Other Fees</p>
	
	<p class="large">Late Payment Fee</p>
	<p class="small">10% of the semester total fee (applied only when past due).</p>
	<p class="large">Late Registration Fee</p>
	<p class="small">$500 (applied when student arrives on campus late (missing orientation) or when not attended scheduled rotation.</p>
	<p class="large">Deferment Fee</p>
	<p class="small">A $500 non-refundable fee to be paid with any request to defer the matriculation date after payment of the enrollment fee.</p>
	<p class="large">Medical Malpractice Insurance</p>
	<p class="small">Medical Malpractice Insurance through WUHS is a requirement of all clinical medicine students.  Medical Malpractice Insurance will be charged during MD6 and MD9 semesters of clinical program.  The current cost of medical malpractice insurance is $1200.00 and must be paid in full by the medical student to be eligible for clinical rotation participation.  This fee may change during a program period based on premiums paid by the University.</p>
	<p class="large">Student Scrubs and Lab Coat Purchase (required)</p>
	<p class="small">Students are required to purchase a set of embroidered scrubs and an embroidered Lab Coat.  Scrubs are required attire to all school labs.  Embroidered scrubs and lab coat are required attire to all off campus learning facilities.  Embroidered lab coats are required attire for students completing clinical rotations.  All students are required to purchase their embroidered set of scrubs and embroidered lab coat.  Please contact the admissions department for more information.</p>
	<p class="large">Financial Aid Application Fee</p>
	<p class="small">$200 (non-refundable) loan processing fee.</p>
	<p class="large">Average Cost of Living</p>
	<p class="small">Cost of living varies from student to student but on average you will need to budget about $600 US to $1000 US per month however roommates, conserving their utilities, cooking at home, and budgeting daily spending will help bring these costs down. The cost of transportation will vary depending on the ways to commute. Every student has different needs, so these amounts are ONLY meant as an average.</p>
	<p class="large">Payment Options</p>
	<p class="small">The Bursar’s Office is responsible for billing and collection of student fees. Payments may be made by credit/debit card, cash, money order, bank draft, or wire transfer. A student approved for loans but waiting for disbursement may provisionally register for classes.</p>
