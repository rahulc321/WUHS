<ul><li>
	<p>Fill out an application online.</p>
	</li>
	<li>
	<p>A nonrefundable processing fee of $100.00 will be charged to the credit card provided on the application for admission. Applications submitted without this information will not be processed.</p>
	</li>
	<li>
	<p>Official transcript from each college or university attended for all undergraduate and graduate studies. The transcripts must evidence that the applicant has a general academic index of 2.75 or above on a scale where A=4.0. Applicants must complete a minimum of ninety (90) credits at an accredited institution of higher education. A baccalaureate degree is highly recommended. </p>
	</li>
</ul>
