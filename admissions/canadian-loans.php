      <h1>Canadian Loans</h1>
	  <p class="small">Most major banks in Canada offer students studying outside Canada a student line of credit. These educational lines of credit are specifically designed to fund the costs associated with the student’s studies.
      For students studying outside of Canada, most lenders require students to have a credit worthy cosigner in order to obtain an approval. Approvals are made at the discretion of the local branch where the application is submitted. Thus, students are advised to apply at the location they and/or their cosigners conduct most of their banking transactions. </p>
      <h1>Private Student Loan Alternatives For Canadian Students</h1>
	  <p class="small">NBC – National Bank of Canada</p>
	  <p class="small">STUDENT LINE OF CREDIT</p>
	  <p>National Bank of Canada offers students a credit line up to $200,000 at a variable rate of Prime with no account handling charges.
     General Program Elements Include:</p>
    <ul><li style="border-left: 1px solid #ffffff !important;">
	<p class="small">A flexible lending solution that allows you to craft your own financial package including the ability to:</p>
	</li>
	<li>
	<p class="small">Defer principal and interest payments on your line of credit while enrolled as a full time student and up to 12 months following graduation (interest will continue to accrue on the outstanding balance).</p>
	</li>
	<li>
	<p class="small">Or make interest and principal payments in accordance with your budget.</p>
	</li>
	<li>
	<p class="small">Draw funds from your account at anytime up to the maximum annual amount.</p>
	</li>
	<li>
	<p class="small">Convert the line of credit to a standard loan with an amortization period of up to 15 years.</p>
	</li>
	<li>
	<p class="small">You also receive a full line of complimentary services with your line of credit.</p>
	</li>
	<li>
	<p class="small">Cheques and Pre-authorized debits.</p>
	</li>
	<li>
	<p class="small">Full range of electronic banking services.</p>
	</li>
	<li>
	<p class="small">200 Free Cheques annually.</p>
	</li>
	<li>
	<p class="small">A National Bank Platinum Card</p>
	</li>
	<li>
	<p class="small">Access to a financial advisor to assist you with planning and developing annual budgets.</p>
	</li>
	<li>
	<p class="small">Additional Information: Visit NBC web site for more information.</p>
	</li>
	
    </ul>
	<p class="small">Application Process:</p>
	<p>Contact the branch nearest your home and make an appointment. You will need to provide proof of full time enrollment at WUHS (Belize). This can be a notice of registration or copy of fall schedule. NBC has 475 branches which are concentrated in Quebec. Branches are located in other provinces including Ontario and British Columbia. The bank web site provides a branch locator tool.</p>
	<p class="large">T.D. CANADA TRUST</p>
	<p class="small">STUDENT LINE OF CREDIT</p>
	<p>TD Canada Trust offers financial assistance via their “Student Line of Credit” These programs are customized to satisfy the individual needs of each student. TD Canada Trust does not maintain a centralized education funding team. They recommend students visit branch locations and discuss funding alternatives with the branch manager. Depending on previous relationship and credit worthiness, TD Canada Trust may modify normal program terms to better accommodate student needs.
    Line of Credit can be used for expenses related to:</p>
	<ul><li style="border-left: 1px solid #ffffff !important;">
	<p class="small">Tuition</p>
	</li>
	<li>
	<p class="small">Books</p>
	</li>
	<li>
	<p class="small">Fees</p>
	</li>
	<li>
	<p class="small">Living Expenses</p>
	</li>
	
    </ul>
	<p class="small">General Program Elements Include:</p>
	<ul><li>
	<p class="small">Pay no monthly or annual fees.</p>
	</li>
	<li>
	<p class="small">Competitive interest rate (Normally equal to TD Prime plus a variance rate).</p>
	</li>
	<li>
	<p class="small">Withdraw funds as the needs arise (up to your approved credit limit).</p>
	</li>
	<li>
	<p class="small">Pay interest-only monthly payments for up to 12 months after graduation.</p>
	</li>
	<li>
	<p class="small">Enjoy low monthly payments after the interest-only grace period.</p>
	</li>
	<li>
	<p class="small">Up to 20 years to repay.</p>
	</li>
	<li>
	<p class="small">May require an approved Co-signer.</p>
	</li>
