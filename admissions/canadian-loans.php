	<h1>RBC Canada</h1>
	<p class="large">ROYAL CREDIT LINE® FOR STUDENTS – PROFESSIONAL DESIGNATION</p>
	<p>RBC Canada offers students a credit line to fund the educational needs. They recommend students contact the bank by phone and complete the application process. RBC Canada encourages students attending international colleges or universities to contact them. The terms and structure of the program do not allow for customization. Students with an existing relationship with RBC may score higher on the overall credit evaluation.
    Line of Credit can be used for expenses related to:</p>
	<ul>
	<li style="border-left: 1px solid #ffffff !important;">Tuition</li>
	<li>Books</li>
	<li>Fees</li>
	<li>Living Expenses</li>
	<li>Travel</li>
	</ul>
	<p>General Program Elements Include:</p>
	<ul>
	<li style="border-left: 1px solid #ffffff !important;">Pay no monthly or annual fees.</li>
	<li>Competitive interest rate (RBC Prime + 1% Variable Rate).</li>
	<li>Withdraw funds as the needs arise.</li>
	<li>Pay interest-only monthly payments for up to 12 months after graduation.</li>
	<li>Enjoy low monthly payments after the interest-only grace period.</li>
	<li>Up to 20 years to repay.</li>
	<li>Will require an approved Co-signer.</li>
	</ul>
	<p>Access funds through:</p>
	<ul>
	<li style="border-left: 1px solid #ffffff !important;">Internet banking</li>
	<li>Royal Direct telephone banking</li>
	<li>Cheques</li>
	<li>RBC Royal Bank Personal Touch Automated Banking Machine</li>
	<li>RBC Canada Branch</li>
	
	</ul>
	<p>Medical Students Only:</p>
	<ul>
	<li style="border-left: 1px solid #ffffff !important;">No annual re-application</li>
	<li>Maximum Credit Line Value – $150,000</li>
	</ul>
	<p>RBC Personal Banker will establish annual maximum available based on documentation of annual costs.</p>
	<p><strong>Additional Information:</strong> Visit RBC Canada web site for more information.</p>
	<p><strong>Contact Options:</strong> </p>
	
	<p><strong>Application Process</strong> </p>
	<p>RBC Canada recommends that you complete the application process via telephone or internet. Students must provide: </p>
	<ul>
	<li style="border-left: 1px solid #ffffff !important;">Confirmation that you are enrolled at a post-secondary international educational institution and pursuing a medical degree.</li>
	<li>An estimate of your education costs (including tuition, supplies, fees, accommodations, food, travel and other qualified expenses).</li>
	<li>A list of your financial resources (including savings, bursaries, summer employment, etc).</li>
	<li>Allow 5 business days from time of application for review, approval and funding of line.</li>
	
	</ul>
	<h1>BOM – Bank of Montreal</h1>
	<p><strong>STUDENT LINE OF CREDIT</strong> </p>
	<p>Bank of Montreal offers students a credit line to fund the educational needs. They recommend students contact the bank by phone and discuss their needs with the education lending team. Bank of Montreal encourages students attending international colleges or universities to contact them. The terms and structure of the program do allow for a modest amount of customization. Students with an existing relationship with BOM may experience more flexibility on the part of the bank.
    Line of Credit can be used for expenses related to:</p>
	<ul>
	<li style="border-left: 1px solid #ffffff !important;">Tuition</li>
	<li>Books</li>
	<li>Fees</li>
	<li>Living Expenses</li>
	<li>Travel</li>
	
	</ul>
	<p>General Program Elements Include:</p>
	<ul>
	<li style="border-left: 1px solid #ffffff !important;">Pay no monthly or annual fees.</li>
	<li>Competitive interest rate (BOM Prime plus Variable Rate).</li>
	<li>Withdraw funds as the needs arise to annual and lifetime maximum.</li>
	</ul>
	<p><strong>Repayment plan for Medical Student Line of Credit:</strong> </p>
	<p>Make only monthly interest payments while you’re in school and during your residency (to a maximum of 7 years of residency).
     In first year after completion of residency, choose one of three repayment options:</p>
	 <ul>
	<li style="border-left: 1px solid #ffffff !important;">Interest only payments during the first year of residency followed by gradually escalating monthly payments of principal and interest.</li>
	<li>Gradually escalating monthly payments of principal and interest.</li>
	<li>Fixed monthly payments of principal and interest based on an amortization of up to 15 years.</li>
	</ul>
	<p>Repay all of the outstanding balance at any time without penalty – will require an approved Co-signer.</p>
	<p>Access funds through:</p>
	<ul>
	<li style="border-left: 1px solid #ffffff !important;">Cheques</li>
	<li>Automated Banking Machine</li>
	<li>BMO Bank of Montreal Branch</li>
	</ul>
	<p>Medical Students Only:</p>
	<ul>
	<li style="border-left: 1px solid #ffffff !important;">No annual re-application</li>
	<li>Maximum Credit Line Value – $150,000 (may be negotiable depending on student and co-signer credit worthiness and proof of need)</li>
	</ul>
	<p>Disbursement</p>
	<ul>
	<li style="border-left: 1px solid #ffffff !important;">Annual Maximum: $30,000 (Fist 4 Years)</li>
	<li>Annual Maximum: $15,000 (Residency)</li>
