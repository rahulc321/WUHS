      <h1 class="large">PRE-CLINICAL SCIENCES</h1>

<p class="large">Our Pre-Clinical Sciences curriculum consists of lectures, research, and lab-based teaching at our state-of-the-art campus in San Pedro.  All courses are designed in the USMLE format, and study is supplemented with hands-on clinical training at the local hospital whenever possible.</br>Small class sizes offer you one-on-one attention from our experienced, enthusiastic professors. Tutoring and mentoringare available as well as a supportive student community that offers plenty of opportunities for peer-led learning groups.</p>

