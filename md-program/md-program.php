      <h1 class="large">Our MD Program</h1>

<p class="large">Washington University of Health and Science (Belize) offers Basic Sciences and Clinical Sciences curricula which are designed to prepare students for licensure and practice in the US or Canada.</p>

<hr /><h4>Program Overview</h4>

<p>Our MD Program consists of 11 semesters (MD1-MD11) that are divided as follows:</p>

<hr /><h4>Basic Sciences (MD1 – MD5)</h4>

<p><strong>MD1 – MD4: Foundations of Medicine</strong></p>

<p>This segment of the Basic Sciences program is conducted in our School of Medicine campus, located in San Pedro, Belize. The duration of the study is approximately 16 months. </p>
<p><strong>MD5: Introduction to Clinical Medicine</p>
<p>After completing the Foundations of Medicine portion, students are eligible to study for a single semester in the United States, in preparation for their upcoming clinical training. This segment introduces students to Clinical Medicine in addition to Physical Diagnosis. A USMLE Review is also given in this semester.</p>

<hr /><h4>Clinical Sciences (MD6 – MD11)</h4>

<p>After passing our Basic Sciences program, students are eligible to to enter clinical clerkships, in rotation, in several specific fields. There is a total of 76 weeks of required clinical core clerkships and electives. All clinical training is conducted in our affiliate hospitals in the United States.</p>

<hr /><h4>Premed Program</h4>
<p>Our Premed Program qualifies students who do not meet any or all of the academic requirements for the MD Program at WUHS. The Premed Program consists of two-semesters coursework in each of the following:</p>

    <ul>
	<li>Biology</li>
	<li>Chemistry</li>
	<li>Organic Chemistry</li>
	<li>Physics</li>
	<li>Mathematics</li>
	<li>English</li>
	</ul>
	
<hr /><h4>Objective of the Program</h4>	
<hr /><h4>Patient Care</h4>	
	
<p>The graduate is able to:</p>
   <ul>
	<li>Approach the care of patients as a cooperative endeavor, integrating patients’ concerns and ensuring their health needs are addressed.</li>
	<li>Comprehensively evaluate patients by obtaining accurate and pertinent medical histories; conducting appropriate and thorough physical examinations; gathering detailed ancillary information; and synthesizing all relevant data to generate prioritized differential diagnoses and formulate plans of care that reflect an understanding of the environment in which health care is delivered.</li>
	
	</ul>
<hr /><h4>Medical Knowledge</h4>	
	
<p>The graduate is able to:</p>
   <ul>
	<li>Demonstrate a broad working knowledge of the fundamental science, principles, and processes basic to the practice of medicine and apply this knowledge in a judicious and consistent manner to prevent common health problems and achieve effective and safe patient care.</li>
	<li>Understand the clinical relevance of scientific inquiry and demonstrate the ability to evaluate emerging knowledge and research as it applies to diagnosis, treatment and the prevention of disease.</li>
	
	</ul>
<hr /><h4>Practice-Based Learning</h4>	
	
<p>The graduate is able to:</p>
   <ul>
	<li>Evaluate the performance of individuals and systems to identify opportunities for improvement.
      Seek out and apply best practices, measure the effect of changes and develop strategies to improve performance.</li>
	
	</ul>
<hr /><h4>Interpersonal Communications</h4>	
	
<p>The graduate is able to:</p>
   <ul>
	<li>Demonstrate leadership and collaborate effectively with other healthcare team members and professional associates.
        Understand how human diversity may influence or interfere with exchange of information.</li>
	
	</ul>
<hr /><h4>Systems-Based Practice</h4>	
	
<p>The graduate is able to:</p>
   <ul>
	<li>Understand the institutions and individuals that participate in healthcare delivery and the role of the physician in the health care system.</li>
	 <li>Appropriately use system resources and assist patients in accessing health care that is safe, effective, patient-centered, timely, efficient and equitable.</li>
	
	</ul>

