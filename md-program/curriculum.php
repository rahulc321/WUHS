	<hr /> <h1 >Course Descriptions</h1>
	<hr /><h4>Semester I</h4>
	<p><strong>MD 100 Anatomy (6 Credits) & MD 100L Anatomy Lab (4 Credits) â 10 Total Credits</strong></p>
	<p>Anatomy focuses on the gross structure of organs and functions and, through clinical correlations, relates each to clinical medicine. An Anatomical Learning Resource Center has been established to utilize computer-based instruction, anatomical models, radiographic materials as well as supervised laboratory sessions dissecting various parts of the human body. Students study the structure and function of all organs with some interaction with cellular structure.</p>
	<p><strong>MD 103 Histology and Cell Biology (6 Credits) & MD 103L Histology Lab (4 Credits) â 10 Total Credits</strong></p>
	<p>This course centers on the study of the microscopic structure of normal human cells, tissues, and organs. Virtual microscopy is used to study the structure of basic tissue types and their integration into organs and organ systems. The lectures correlate microscopic and gross anatomy with basic histophysiology and function of organ systems. On completion, the student must be able to identify, describe, and give function of cells, tissues, structures, and organs of the human body presented via lecture and digital imagery. Students must complete specific performance objectives which accompany individual lecture segments, and, where appropriate, be able to integrate histology with other classes of the curriculum. This course develops the necessary understanding of how the cell functions at the cellular, organelle and molecular levels. Students are exposed to a wide variety of topics, such as cell structures and their functions, membrane transport, signal transduction, DNA replication and repair, transcription, translation, regulation of gene expression, cancer and molecular biology techniques.</p>
	<p><strong>MD 104 Biochemistry (6 Credits) & MD 104L Biochemistry Lab (4 Credits) â 10 Total Credits </strong></p>
	<p>This course focuses on the interrelationship and regulation of metabolic pathways as it pertains to understanding the mechanism of disease states. The student is prepared accordingly through a discussion of the principles of biochemistry including anabolic and catabolic reactions as permitted by the generation and use of energy. Biochemical mechanisms are utilized to justify particular signs and symptoms noted in certain clinical conditions. In so doing a comprehensive understanding of the metabolism of Proteins, Carbohydrates, Lipids and other Nitrogen containing molecules is achieved.</p>
	<p><strong>MD 106 Medical Terminology â 4 Credits</strong></p>
	<p>This course focuses on the language of medicine. Medical Terminology is like learning a new language. Medical terms are derived from Greek and Latin. Understanding the terms will be easier when you learn the way to analyze the elements of the word. Most medical terms have a ârootâ which signifies a disease, procedure or body part. The word then has a âprefixâ and/or âsuffixâ which provides you more information as to the exact location, position, specific procedure, etc. When you know how to break the word, you have are able to gain an immense amount of information even if youâve never seen the word before.</p>
	<hr /><h4>Semester II</h4>
	<p><strong>MD 201 Physiology (6 Credits) & MD 201L Physiology Lab (4 Credits) â 10 Total Credits</strong></p>
	<p>Physiology concentrates on how the various organ systems that comprise the human body function. The major objective of this course is to enable the student to acquire a sound understanding of the mechanisms upon which life depends through an integrated study of the many control systems that maintain homeostasis. Emphasis is placed on the mechanisms that maintain a homeostasis under a variety of conditions. The course begins with a study of basic physiological principles, such as, the transport of ions, intracellular signaling, osmosis, membranes and their electrical properties. Following the presentation of the basic principles of cellular physiology, which includes muscle and nerve, cardiovascular, respiratory, renal, gastrointestinal, endocrines, reproductive physiology are taught and integrated into total body function. Temperature regulation and the integrated physiological responses to exercise and adverse environments are also presented.</p>
	<p><strong>MD 203 Behavioral Science â 6 Credits</strong></p>
	<p>Behavioral Sciences stresses the complex relationship between psychological make-up and experience, by providing a knowledge base for normative and non-normative human development throughout the life cycle. The course also introduces the student to the behavioral basis of clinical medicine by focusing on common behavioral problems and the circumstances that evoke important behavioral / emotional responses.</p>
	<p><strong>MD 202 Neuroscience/Neuroanatomy â 6 Credits</strong></p>
	<p>Neuroscience begins with an overview of the entire nervous system. As the course progresses, the focus is on comprehending the basic structure and function of each level of the nervous system, integrating both the anatomy and physiology of the nervous system. The principles that underlie the anatomical structure of each system of the brain are correlated with its physiology; correlations between the functional deficits and the pathological anatomy in several neurological diseases which require working knowledge of anatomy and physiology are stressed. Special attention is given to integrating current understandings of human neurological and psychiatric diseases, and each topic is supplemented by relevant lab exercises which include detailed brain dissection and exposure to angiograms, CT scans, MRI, etc.</p>
	<p><strong>MD 206 Medical Nutrition â 4 Credits</strong></p>
	<p>Medical Nutrition focuses on the nutritional aspect of medicine. Medical Nutrition is the evaluation of an individualâs nutritional status bases on the interpretation of clinical information. Nutrition assessment is an important tool in clinical medicine because malnutrition (both obesity and under nutrition) is a common clinical finding. Many patients can benefit from medical nutrition therapy (MNT) using established evidence-bases protocols. The purpose of nutrition assessment is to accurately evaluate an individualâs dietary intake and nutritional status; and determine if medical nutrition therapy and/or counseling is needed; and monitor changes in nutritional status; and evaluate the effectiveness of nutritional interventions</p>
	<hr /><h4>Semester III</h4>
	<p><strong>MD 302 General Pathology (6 Credits) & MD 302L General Pathology Lab (4 Credits) â 10 Total Credits</strong></p>
	<p>Pathology introduces students to the cellular system of each organ and traces the morphological changes in a cell that are responsible for a disease in an organ. As cells undergo alteration, their change in function is studied in respect to its deviation from the ânormalâ state. Course presentation includes the response of cells, tissues and organs to disease and injury; the normal and adapted cell; degeneration and necrosis, inflammation, fluid and hemodynamic derangements; neoplasia; immunopathology; systemic, environmental and nutritional disease. Lecture discussions are supplemented by a study of gross and microscopic specimens.</p>
	<p><strong>MD 300 Microbiology (6 Credits) & MD 300L Microbiology Lab (4 Credits) â 10 Total Credits</strong></p>
	<p>Microbiology teaches students the basic concepts of infectious disease in a lecture and laboratory setting. The goal of the course is for students to gain a basic knowledge and understanding of microbial diagnosis of Bacteria, Viruses, Fungus, Protozoa and Parasites. The etiology, pathogenesis and genetics of bacterial infection are key foundations to the study of microbes. Students will learn the symptoms that help in diagnosis of a patient and how these symptoms relate to disease. </p>
	<p><strong>MD 301 Immunology â 6 Credits</strong></p>
	<p>The course begins with a general overview and introduction to the immune system including a description of the cells and tissues involved with innate and adaptive immunity. This is followed by descriptions of the molecular and cellular mechanisms employed in innate immune responses, and for those used in the humoral and cell-mediated arms of adaptive immunity. This includes the details of antigen processing and presentation by antigen presenting cells and the central role of MHC molecules in this process. The maturation and selection of B and T lymphocytes and the production of the diverse antigen receptors required for lymphocyte activation are also described in detail. The pathways of lymphocyte activation are followed by an explanation for the generation of the different effector functions and memory cells produced during a humoral or cell-mediated response.</p>
	<p><strong>MD 303 Medical Genetics â 6 Credits</strong></p>
	<p>This course provides a description of the human genome including the details of DNA, gene, and chromosome structure, the basics of gene expression, and the various forms of inheritance. The overall goal is to use this knowledge to better understand the molecular mechanisms of how genetic mutations lead to the single gene and complex disorders described in the textbook case studies. Specific course topics include gene mapping and disease gene identification, the treatment of genetic disease, prenatal diagnosis, cancer genetics, and pharmacogenetics.</p>
	<p><strong>MD 306 Epidemiology and Biostatistics â 4 Credits</strong></p>
	<p>The principles of biostatistics are introduced in this course, emphasizing both the practice of interviewing and collecting data. The epidemiology of disease and concepts of Public Health and Industrial Medicine are also covered in this course. Finally, the course will end with discussions of broad issues related to health care delivery, health care legislation and costs, and a comparative discussion of health care systems.</p>
	<p><strong>Epidemiology</strong></p>
	<p>The course has been designed to provide the student with a broad understanding of the concepts and principles of epidemiology with emphasis on its role in health, medicine and research. They will also be taught and involved in the data collection, tabulation and summarization and presentation of data and reporting. Topics include research methods, study designs, sampling, data analysis, the interpretation of data, and the application of findings in health setting.</p>
	<p><strong>Biostatistics</strong></p>
	<p>Biostatistics is the study and development of statistical, mathematical, and computational methods applied to the biological, health, and human sciences. Biostatisticians play a key role in the design, conduct, and analysis of research studies in the areas of health and disease, and create and apply methods for quantitative research in health related fields. Topics covered in this course include: data description, probability, distribution of random variables, applications of the binomial and normal distributions, estimation and confidence intervals, hypothesis testing, contingency tables, regression, and analysis of variance. Additional topics include introduction to statistical computing and data management, non-parametric, or distribution free, statistical methods and demographic measures. Students will need to use a statistical program (Microsoft Excel Â® or other program) to assist with computations.</p>
	<hr /><h4>Semester IV</h4>
	<p><strong>MD 402 Systemic Pathology (6 Credits) & MD 402L Systemic Pathology Lab  (4 Credits) â 10 Total Credits</strong></p>
	<p>Pathology II applies the basic concepts learned in Pathology I to continue the study of pathologic basis of disease using a physiologic system, or organ-based approach. This course covers red and white cell diseases, male and female genital tracts, and kidney and liver systems.</p>
	<p><strong>MD 401 Pharmacology â 6 Credits</strong></p>
	<p>This course concentrates on how chemical agents (drugs) regulate or modify physiological functions of the body, demonstrating how interactions of drugs with living organisms contribute to diagnosis, prevention, treatment or cure of diseases. Biologic responses, physiological alterations and correction of disorder or disease are discussed for each drug class highlighting receptor interaction, which defines the agentâs boundaries of efficacy.</p>
	<p><strong>MD 406 Medical Ethics â 4 Credits</strong></p>
	<p>Medical Ethics is designed to introduce ethical, professional and legal issues that arise in the practice of medicine. This course provides an overview of the salient issues for students, tools used to recognize ethical, professional and legal conflicts in clinical settings, and resources to critically examine and address questions and concerns these conflicts present in patient care.</p>
	<hr /><h4>Semester V</h4>
	<p><strong>MD 501 Introduction to Clinical Medicine â 6 Credits</strong></p>
	<p>This course is designed to introduce students to the basic skills they will need to function as effective clinicians. History taken and physical examination skills are taught in practical classes using the latest technological media, including stimulators (adult, pediatric and adolescent). The course addresses a range of clinical skills necessary for the future development as a physician, including clinical assessment and plan for the care of patients using library and computer search of evidence based information for patient care.</p>
	<p><strong>MD 500 Physical Diagnosis â 6 Credits</strong></p>
	<p>Physical Diagnosis I is designed to provide early exposure to clinical medicine. Students are instructed in patient interviewing and communication skills. They also receive hands on examination skills in the musculoskeletal system using simulated and standardized patients. Additionally, clinical correlations and medical imaging are presented in conjunction with the Gross and Developmental Anatomy course. Professionalism in doctor-patient, doctor-doctor, and doctor-society interaction is stressed.</p>
	<p><strong>MD 502 USMLE Review â 6 Credits</strong></p>
	<p>Major emphasis of these classes will be to familiarize the student to the pattern of questions that are asked in USMLE step I. The classes are not meant to review the subjects in depth but to integrate the knowledge gained from various Basic Science subjects and give an over view as to how to apply such knowledge in the clinic and to answer clinically relevant topics. For instance, anatomy, physiology, biochemistry, pharmacology and pathology (including immunology, microbiology, and molecular biology) as applied to each organ system will be reviewed rapidly and the relevant diseases pertaining to the system under discussion will be discussed. For example, while reviewing the endocrine system- anatomy of various endocrine organs, their physiological function, and biochemical aspects of the synthesis of various hormones will be discussed including embryology, and a brief mention of various endocrine diseases will be mentioned with emphasis on the pathological processes involved and their treatment including pharmacology of the drugs used in these diseases. The same pattern will be followed for other organ systems and their diseases.</p>
	<p>Since major emphasis in USMLE Step 1 examination is to ask questions that are clinically relevant, such a review covering various basic and clinical aspects of all the organ systems will familiarize the student as to how to prepare for the examination and, in turn, face the test with more confidence. In addition, such a preparation will teach the student as to how to apply knowledge of basic sciences to clinically relevant situations and arrive at the right decision both in making the correct diagnosis and applying right therapeutic.</p>
      




