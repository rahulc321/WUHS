      <p class="large">What sets the WUHS clinical program apart is the personalized, hands-on support provided by WUHS faculty at every stage of a student’s clinical rotation.</p>


<p><strong>SMALL CLASS . BIG SUPPORT.</p>
<p>WUHS small class size, combined with our emphasis on personalized learning, means you’ll receive a level of support and guidance during your clinical rotations that simply can’t be matched by bigger schools—particularly those with classes three and four times as big as WUHS.At every step of your clinical rotations, WUHS faculty will be in contact with your attending physicians and preceptors to monitor your progress.</p>
<p><strong>CLINICAL, PROFESSIONAL, & PERSONAL</p>
<p>WUHS objective is not only to make sure each student maximizes his or her clinical experience, but also focuses on career planning and personal growth. Additionally, WUHS works with students on the vital administrative details that come with clinical rotations, such as housing and scheduling. The result is that WUHS students are able to focus fully on their clinical, professional, and personal development during this critical phase of their education, knowing the WUHS team is behind them.</p>
<p><strong>HAND-PICKED PROGRAMS & NO WAITING</p>
<p>WUHS extensive clinical network ensures that there is no waiting period for clinical rotations.</p>
