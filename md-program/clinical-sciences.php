<p><strong>Core Clinical Rotations Total 48 weeks</p>
<p><strong>INTERNAL MEDICINE (12 WEEKS)</p>
<p>The Internal Medicine Clerkship is 12 weeks long where students study with the prevention, diagnosis, and treatment of the diseases in adulthood, including the management of undifferentiated and multi-system diseases. Clinical problem-solving in internal medicine is based on the integrated pathophysiological, psychosocial, epidemiological and all other “bedside” information to address urgent problems, manage chronic illness, and promote health, using the best scientific evidence to patient care. The Internal Medicine Clerkship is a student’s main opportunity to improve his or her fundamental skills of data collection, clinical reasoning and understanding of the pathophysiological process in clinical settings, and to become familiar with the common acute and chronic illnesses in adults, their diagnoses and treatment as well as with screening methods and preventive medicine activities.</p>
<p><strong>SURGERY (12 WEEKS)</p>
<p>The 12-week General Surgery Clerkship is designed to be an introduction to surgery and selected surgical subspecialties.  Students are taught the approach to the surgical patient and participate in pre and postoperative care as well as perform certain invasive techniques safely. By the end of rotation, students are expected to know the indications and the contradictions and the role of ancillary services in managing surgical diseases.</p>
<p><strong>OB-GYN (6 WEEKS)</p>
<p>Obstetrics and Gynecology is a 6-week core rotation that provides students the opportunity to participate in comprehensive health care for women from childhood through the adolescence, reproductive and menopause years, and beyond. This clerkship offers both inpatient and outpatient clinical experience and is integrated to provide students with an understanding of routine OB/GYN care performed in the physician’s office. The primary focus is obstetrical conditions and gynecological problems commonly encountered by the physician. Students will observe and perform selected procedures necessary to provide antepartum, post-partum, and pre- and post-operative care of gynecological patients. Students will be required to take call, attend conferences, and read suggested literature under the supervision of attending physicians.</p>
<p><strong>PEDIATRICS (6 WEEKS)</p>
<p>The Pediatric Clerkship is a 6-week course that addresses health care issues unique to infants, children, and adolescents. It integrates biology, clinical and behavioral sciences with psychosocial, cultural, family, community and other influences on children’s health and well-being, within the framework of expected growth and development.  Students will have the opportunity to participate in the clinical activities of both general and subspecialty pediatric services, to study the impact of the disease and treatment of the disease on growth and development, and on patient and family and community. They will also have the opportunity to participate in health supervision and recognition of common health problems, in the prevention of disease and injury and other activities relevant to pediatric issues. </p>
<p><strong>PSYCHIATRY (6 WEEKS)</p>
<p>The Psychiatry Clerkship is a 6-week course. Students will have the opportunity to participate in the studies and treatment of various common affective, behavioral, cognitive, and perceptual disorders. Students will experience psychiatry through clinical hands-on experience, directly, or as a part of consult/liaison team, through patient encounters, through clinical teaching and a structured didactic program and under the guidelines and supervision of the attending physicians. The students’ clinical experience will be supplemented by the directed readings, discussions with preceptors, completion of required written reports, case presentations, didactic presentations and student readings appropriate to the care of the student’s own patients. </p>
<p><strong>FAMILY MEDICINE (6 WEEKS)</p>
<p>Family Medicine is a 6-week clerkship for third-year medical students. It encompasses continuing, comprehensive care of patients of all ages, both genders, and of their families as well. Family medicine integrates biological, clinical, social, and behavioral sciences with psychosocial, cultural, family, community, and other influences on people’s health. Under the guidelines and the supervision of faculty/preceptors and attending physicians, students achieve the competencies in the field of family medicine primarily through ambulatory clinical experience, with the main focus on the management of chronic diseases, health maintenance, and undiagnosed new problems. During this rotation, students integrate, expand, and refine their clinical skills and become familiar with the coordinated health care provided by a family practitioner. </p>
