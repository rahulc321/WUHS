<p><strong>CLINICAL ROTATIONS – 76 WEEKS</p>
<p>The Clinical Sciences component of our top-quality MD Program is completed over six semesters (76 weeks) at affiliated hospitals and clinical facilities in the US. Clinical training at Washington University of Health and Science is divided into 48 weeks of core clerkships and 28 weeks of elective clinical rotations. The clinical faculty teaches critical components of the medical school curriculum and supervises students and residents on clinical rotations.During the Clinical Sciences, medical school students participate in patient care in hospitals and clinics while rotating through various medical specialties. Training includes physical examination, laboratory analysis, case presentations, clinical workshops, and conferences. Medical students receive personalized guidance from the clinical faculty. This practical experience is the best way to develop the skills and knowledge needed for residency.</p>
