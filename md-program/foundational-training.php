      <p class="large">The Practice of Medicine program provides broad clinical science education in the first two years with early exposure to patient care and the practice of medicine. The program employs a variety of instructional methods to equip students with the critical thinking and communication skills essential   to providing compassionate, expert care. These include clinical problem-based cases, videotaped simulations with standardized patients and computer-based instruction.</p>
	  </hr><h4>Early Hands-On Experience</h4>
	  <p>In addition to early patient exposure through the Practice of Medicine program, The Clinic at the campus provide a remarkable opportunity to gain hands-on experience beginning in the preclinical years.  By volunteering at the clinics, students acquire basic clinical skills and learn how to work closely with one of the most diverse populations in the country.</p>
	  </hr><h4>Broad Clinical Exposure</h4>
	  <p>Through an extensive schedule of clerkships, students learn how to work closely with one of the most diverse populations in the country, while gaining exposure to a range of chronic and acute conditions under the mentorship of leading experts. Participating hospitals including locations in Denver   , Atlanta , Miami , and Chicago</p>
	  </hr><h4>Global & Community Health</h4>
