	  </hr><h4>Small Class . Big Support.</h4>
	  <p>WUHS  small class size, combined with our emphasis on personalized learning, means you’ll receive a level of support and guidance during your clinical rotations that simply can’t be matched by bigger schools—particularly those with classes three and four times as big as WUHS.</br>
At every step of your clinical rotations, WUHS  faculty will be in contact with your attending physicians and preceptors to monitor your progress.</p>

     </hr><h4>Clinical, Professional, & Personal</h4>
	 <p>WUHS  objective is not only to make sure each student maximizes his or her clinical experience, but also focuses on career planning and personal growth. Additionally, WUHS works with students on the mundane yet vital administrative details that come with clinical rotations, such as housing and scheduling.
The result is that WUHS  students are able to focus fully on their clinical, professional and personal development during this critical phase of their education, knowing the WUHS  team is behind them.</br>
That’s a far cry from the experience at other schools, where students are often on their own once they move on from Basic Sciences.</p>
	 </hr><h4>Hand-Picked Programs & No Waiting</h4>
	 <p>WUHS  extensive clinical network ensures that there is no waiting period for clinical rotations.</p>
