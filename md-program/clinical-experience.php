<li><strong>Supportive Environment:</strong> WUHS  small class sizes and high faculty-to-student ratio creates a supportive learning environment that helps students success</li>
<li><strong>Affordability:</strong> WUHS low tuition fees</li>
<li><strong>USMLE Success:</strong> WUHS  students have a first-time pass rate on the critically important USMLE Step 1 exam that’s the equal of U.S. medical schools.</li>
<li><strong>Advanced Campus:</strong> WUHS  has high-tech classrooms, hands-on labs, and simulation activities with the latest multimedia, instructional technology.</li>
 <li><strong>Canada: </strong>WUHS  has a top track record of securingclinical rotations and residencies in Canada.</li>
<li><strong>On-Campus Pre-Med Program:</strong> WUHS pre-med program is taught by the same faculty, and in the same facilities, as our med school program, allowing a seamless transition between programs.</li>
