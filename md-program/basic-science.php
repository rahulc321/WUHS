      <h1 class="large">Basic Science</h1>

<p class="large">Students may purchase study textbooks online through WUHS (Belize’s) Online University Bookstore at very competitive prices. Furthermore, all medical equipment and school supplies such as dissection kits, lab coats, scrubs, Video CD’s and more may be ordered at the Online University Bookstore.</br>
We strongly encourage all students to purchase their books at our online bookstore to have them delivered directly to the campus without a hassle carrying them with you.
Please Note: Washington University Health and Science (Belize) reserves the right to change the book list at any time, therefore to prevent unnecessary expenditures, we suggest that students should purchase their books for one semester only.</p>

