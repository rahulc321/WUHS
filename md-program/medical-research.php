	  Research in medicine is lifelong learning and a strong research project will strengthen your residency application.</br>
	  During the Pre-Clinical Sciences at WUHS, you will learn and practice how to understand the research methods thoroughly. You will write a hypothesis and design a research project, how to collect and evaluate data and communicate the knowledge to others, and how to think scientifically and critically for the rest of your professional life.</br>
	  During clinical rotations, you will have the opportunity to participate in research projects and to present clinical and scientific information clearly and cogently, both orally and written to colleagues and other health professionals.</br>
	  You will formally document your engagement in research activity in your graduation paper. Your graduation paper can be in the areas of basic, clinical, or applied medical science.  It can be experimental research, clinical trial, epidemiological, observational, or clinical study or it can be a critical interpretation of the scientific literature based on the principles of evidence-based medicine. At WUHS (Belize) our students will:</p>
