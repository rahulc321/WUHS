      <h1 class="large">Pre-Medical</h1>

<p class="large">The Pre-Med program is comprised of three semesters aimed to prepare students to entry into the Medical Program offered at WUHS.</br>The Pre-Med allows students to earn the required 94 credit hours of sciences required for entry into the Pre-Clinical Program at WUHS.</p>

<hr /><h4>Pre-Medical Course Schedule:</h4>
<hr /><h4>Semester One</h4>
<p>Algebra I (6 credits)</br>
General Biology I (8 credits)</br>
General Chemistry I (8 credits)</br>
Medical Research (6 credits)</p>
<h4>Semester Two</h4>
<p>College Physics I (8 credits)</br>
General Biology I (8 credits)</br>
General Chemistry II (8 credits)</br>
Organic Chemistry I (8 credits)</br>
English Studies (6 credits)</p>
<h4>Semester Three</h4>
<p>Algebra II (6 credits)</br> Organic Chemistry II (8 credits).</br>
College Physics II (8 credits)</br>
Intro to Medical Studies (6 credits)</p>
<h4>Transfer Credit</h4>
<p>Transfer credits will be evaluated on a case-by-case basis.  WUHS reserves the right to refuse transfer credits.  All college-level transfer credits must be submitted with an official transcript.  Accepted grade of “C” or higher in order to gain transfer credit.</p>




