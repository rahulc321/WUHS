	 <hr />
	 <p class="large">LANGUAGE</p>
	 <p>English is the official language and spoken widely; other languages include Spanish, Maya, Carifuna, and Mandarin. Belizeans also speak English-based Creole (Kriol).</p>
    
     <p class="large">TIME ZONE</p>
	 <p>US Central Standard Time is the norm except during Daylight Savings Time when Mountain Time Zone is applicable.</p>
	 <p class="large">ELECTRICITY</p>
	 <p>Power is stable at 110 volts A.C.</p>
	 <p class="large">HEALTH & SAFETY</p>
	 <p>There is a well-staffed hospital and several private doctors in Belize City. In addition, the District Towns and larger Villages have hospitals or clinics. Potable water is available in most areas of Belize, but it is advisable to ask, and if in doubt, to drink boiled or bottled water. There are not severe epidemic diseases in Belize. We recommended anti-malaria tablets for extended stays in the jungle—no immunizations required at the time of entry. Crime is minimal in Belmopan. However, as with anywhere, individuals should be cautious walking alone at night.</p>
	 <p class="large">CLIMATE</p>
	 <p>Belize is in a Subtropical region, which means its weather is primarily hot and humid. There are two seasons: Rainy Season (June to November) and Dry Season (December to May). There are brisk prevailing trade winds from the Caribbean Sea and an average temperature of 80 degrees F.</p>
	 <p class="large">GOVERNMENT</p>
	 <p>Independent from Britain since 1981, Belize (formerly British Honduras) has a democratically elected parliamentary government and is a member of the British Commonwealth.</p>
	 <p class="large">WATER</p>
	 <p>Belize tap water is treated in cities and towns and is safe to drink. Bottled water is also available for purchase throughout the country.</p>
	 <p class="large">CLOTHING</p>
	 <p>Pack light casual clothing, preferably natural materials. Check with your travel representative about attire for adventure activities.</p>
