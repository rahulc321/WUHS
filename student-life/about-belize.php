      <div class="p-rum-accordion--body p-atge-accordion--body p-body">
      <p class="large">Belize is a tropical country that lies west of the Caribbean coast, nestled between Mexico and Guatemala, and is home to the largest barrier reef in the Western Hemisphere. With over 40% of the country protected as parks and natural reserves, studying in Belize offers an educational experience with diverse adventure opportunities and intriguing experiences.
When asked, repeated visitors generally say that they did not have enough time to do all they wanted to do on their first visit. If it is the third or fourth trip, the answer is the same, but with this addition, “it is the warmth and friendliness of the people” that keeps visitors returning year after year. The good news is that while this is all quite true, the country also offers fun places to visit and world-class restaurants. When you first come to Belize, you are a visitor, but you are a friend when you leave.
</p>
     <hr />
	 <p><strong>General Information:</strong><a ></a><a href="https://www.travelbelize.org/" target="_blank">www.travelbelize.org </a></p>
     <p><strong>Geographical:</strong> 22,963 sq km (8,866 sq. miles), slightly larger than Massachusetts.</p>
	 <p><strong>Capital:</strong> Belmopan (pop. 4,500).</p>
	 <p><strong>Currency:</strong> Fixed rate of BZ$2.00 to US$1.00</p>
	 <p><strong>POPULATIONS – 311,480<strong></p>
