            <h2 class="p-rum-accordion--heading p-atge-accordion--heading p-heading">
    Student Supportive Services
    </h2>
	<p class="large">The mission of Student Support Services is to facilitate a supportive environment for academic success and personal enrichment through proactive and individualized services available to a student from their first semester through graduation. </p>
	<hr />
	 <p><strong>At WUHS (Belize), students take charge of their learning and develop skills that will enable them to enhance their lives and become well-balanced citizens. SSS staff initiates and develops partnerships with academically-based students and strives to lead students down a path towards cultural growth and awareness.</strong></p>
	 
	 <p><strong>Eligibility</strong></p>
	    <ul>
		  <li style="border-left: 1px solid #ffffff !important;">Currently enrolled at WUHS (Belize)</li>
		  <li>Our project participants are students who meet at least one of the following criteria:</li>
		    <ul>
		       <li style="border-left: 1px solid #ffffff !important;">First-generation (neither parent has completed a degree at a 4-year college or university</li>
		       <li>Low-income backgrounds; Documented physical, emotional, or learning disability</li>
			   <li>Demonstrate Academic Need for program services</li>
			   <li>Currently enrolled at WUHS (Belize)</li>
		     </ul>
		</ul>
    <hr />
    <p class="large">Services Provided</p>
	<p>The SSS program provides assistance and services like personal counseling, academic advice, academic workshops, career services, activities designed to foster enrollment in graduate programs, study lab, computer lab, and, if requested, mentoring involving faculty or upper-level students.</br>SSS also offers services to students with limited English proficiency. As a result, students can avail of additional services requested by participants or improve program participants' persistence, retention, and graduation rates. </p>
	
	<p class="large">Cost of Program Participation</p>
	<p>There is no cost to students who qualify. However, students who actively and fully participate and complete all program requirements will apply for supplemental financial grant aid. The SSS Director and Program Coordinator can provide prospective students with more details. </p>
	
	<p class="large">Federal Requirements</p>
	<p>Two-thirds of the students served by the WUHS (Belize) Student Support Services program must be low-income individuals who are also first-generation college students or students with disabilities. </p>
	    <ul>
		  <li style="border-left: 1px solid #ffffff !important;">a signed statement from the parent or guardian of a dependent student regarding family income;</li>
		  <li>verification of family income from another government source;</li>
		  <li>a signed financial aid application; or</li>
		  <li>a signed income tax statement.</li>
		</ul>
  
     <p>If the student is applying as an independent student, their records will verify eligibility status.</p> 
  
